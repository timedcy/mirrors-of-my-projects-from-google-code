            mail.parts.each do |m|
                m.base64_decode
                if m.multipart?
                    parse_mail(m)
                else
                    if m.content_type == 'text/plain'
                        puts m.body
                    end
                end
            end
        else
            puts mail.body
        end
end

def main(str)
    mail = TMail::Mail.parse(str)
    parse_mail(mail)
end

